package org.newdawn.spaceinvaders.model;

public class RecordInfo {
	
	private String name;
	private double record;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getRecord() {
		return record;
	}
	public void setRecord(double record) {
		this.record = record;
	}

}
